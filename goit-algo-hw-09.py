### 2) –§—É–Ω–∫—Ü—ñ—è –¥–∏–Ω–∞–º—ñ—á–Ω–æ–≥–æ –ø—Ä–æ–≥—Ä–∞–º—É–≤–∞–Ω–Ω—è find_min_coins


# –ó–Ω–∞–π—Ç–∏, —è–∫ –≤–∏–¥–∞—Ç–∏ –∑–∞–¥–∞–Ω—É —Å—É–º—É –∑ –Ω–∞—è–≤–Ω–∏—Ö –∫—É–ø—é—Ä —Ç–∞–∫, —â–æ–±:
