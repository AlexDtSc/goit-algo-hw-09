# - –∑–Ω–∞–π—Ç–∏ –∫–æ–º–±—ñ–Ω–∞—Ü—ñ—é –∑ –º—ñ–Ω—ñ–º–∞–ª—å–Ω–æ—é –∫—ñ–ª—å–∫—ñ—Å—Ç—é –∫—É–ø—é—Ä.


def find_min_coins(amount, bills):
    """
    amount: int - —Å—É–º–∞, —è–∫—É –ø–æ—Ç—Ä—ñ–±–Ω–æ –≤–∏–¥–∞—Ç–∏
    bills: dict {–Ω–æ–º—ñ–Ω–∞–ª: –∫—ñ–ª—å–∫—ñ—Å—Ç—å} - –Ω–∞—è–≤–Ω—ñ –∫—É–ø—é—Ä–∏
    –ü–æ–≤–µ—Ä—Ç–∞—î dict {–Ω–æ–º—ñ–Ω–∞–ª: –∫—ñ–ª—å–∫—ñ—Å—Ç—å} –∞–±–æ None, —è–∫—â–æ –Ω–µ–º–æ–∂–ª–∏–≤–æ.
    """

    denominations = sorted(bills.keys())
    max_amount = amount + 1

    # dp[i] = None –∞–±–æ —Å–ª–æ–≤–Ω–∏–∫ {–Ω–æ–º—ñ–Ω–∞–ª: –∫—ñ–ª—å–∫—ñ—Å—Ç—å} –¥–ª—è —Å—É–º–∏ i (üìö –î–∏–Ω–∞–º—ñ—á–Ω–µ –ø—Ä–æ–≥—Ä–∞–º—É–≤–∞–Ω–Ω—è (dp) )
    dp = [None] * (max_amount)       # dp[i] ‚Äî —Å–ª–æ–≤–Ω–∏–∫ {–Ω–æ–º—ñ–Ω–∞–ª: –∫—ñ–ª—å–∫—ñ—Å—Ç—å}, —â–æ–± –∑—ñ–±—Ä–∞—Ç–∏ —Å—É–º—É 'i';         —Å—Ç–≤–æ—Ä—é—î–º–æ —Å–ø–∏—Å–æ–∫ —ñ–∑ None –¥–æ–≤–∂–∏–Ω–æ—é –≤ max_amount
    dp[0] = {}                       # dp[0] = {} ‚Äî –±–∞–∑–æ–≤–∏–π –≤–∏–ø–∞–¥–æ–∫: 0 –≥—Ä–Ω ‚Äî —Ü–µ "–ø—É—Å—Ç–∏–π –Ω–∞–±—ñ—Ä –∫—É–ø—é—Ä".    1-—à–µ –∑–Ω–∞—á–µ–Ω–Ω—è —Å–ø–∏—Å–∫—É - –ø–æ—Ä–æ–∂–Ω—ñ–π —Å–ª–æ–≤–Ω–∏–∫, –≤ —è–∫–∏–π –±—É–¥–µ –¥–æ–¥–∞–≤–∞—Ç–∏—Å—è {–Ω–æ–º—ñ–Ω–∞–ª: –∫—ñ–ª—å–∫—ñ—Å—Ç—å}

    for i in range(1, max_amount):                                      # –î–ª—è –∫–æ–∂–Ω–æ—ó —Å—É–º–∏ 'i' –≤—ñ–¥ 1 –¥–æ max_amount = 51 
        for denom in denominations:                                      # –ø—Ä–æ–±—É—î–º–æ –≤—Å—ñ –Ω–æ–º—ñ–Ω–∞–ª–∏ denom, —â–æ–± –ø–æ–¥–∏–≤–∏—Ç–∏—Å—è, —á–∏ –º–æ–∂–Ω–∞ –¥—ñ–π—Ç–∏ –¥–æ 'i', –¥–æ–¥–∞–≤—à–∏ denom –¥–æ –≤–∂–µ –∑—ñ–±—Ä–∞–Ω–æ—ó 'i' - denom.
            if denom <= i and dp[i - denom] is not None:                 # –Ø–∫—â–æ –º–æ–∂–Ω–∞ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞—Ç–∏ denom –¥–ª—è —Å—Ç–≤–æ—Ä–µ–Ω–Ω—è —Å—É–º–∏ 'i' —Ç–∞ dp[i - denom] –≤–∂–µ –º–∞—î —Ä–æ–∑–≤‚Äô—è–∑–æ–∫.
                # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ —î —â–µ –¥–æ—Å—Ç—É–ø–Ω—ñ –∫—É–ø—é—Ä–∏ denom
                prev = dp[i - denom]                                      # –±–µ—Ä–µ–º–æ –≤–∂–µ –Ω–∞—è–≤–Ω—É –∫–æ–º–±—ñ–Ω–∞—Ü—ñ—é prev;
                count = prev.get(denom, 0)
                if count < bills[denom]:                                  # –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –º–∏ —â–µ –Ω–µ –≤–∏—á–µ—Ä–ø–∞–ª–∏ –∫—É–ø—é—Ä–∏ denom.
                    # –§–æ—Ä–º—É—î–º–æ –Ω–æ–≤–∏–π —Å–ª–æ–≤–Ω–∏–∫ –∑ –¥–æ–¥–∞–≤–∞–Ω–Ω—è–º –∫—É–ø—é—Ä–∏ denom
                    new_combo = prev.copy()                               # –§–æ—Ä–º—É—î–º–æ –Ω–æ–≤—É –∫–æ–º–±—ñ–Ω–∞—Ü—ñ—é
                    new_combo[denom] = count + 1

